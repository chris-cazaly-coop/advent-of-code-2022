let formattedInput = input.split("\n"); // ['A Y', B X', 'C Z']

let gameValues = {
  A: 1,
  B: 2,
  C: 3,
  LOSE: 0,
  DRAW: 3,
  WIN: 6,
  ROCK: "A",
  PAPER: "B",
  SCISSORS: "C",
  X: "LOSE",
  Y: "DRAW",
  Z: "WIN",
};

const gamePoints = {
  A: 1,
  B: 2,
  C: 3,
  X: 0,
  Y: 3,
  Z: 6,
};

let score = 0;

const chooseShape = (opponentShape, result) => {
  if (opponentShape == "A") {
    // rock
    if (result == "LOSE") return "C"; // scissors
    if (result == "DRAW") return "A"; // rock
    return "B";
  }
  if (opponentShape == "B") {
    // paper
    if (result == "LOSE") return "A"; // ROCK
    if (result == "DRAW") return "B"; // rock
    return "C";
  }
  if (opponentShape == "C") {
    // scissors
    if (result == "LOSE") return "B"; // PAPER
    if (result == "DRAW") return "C"; // rock
    return "A";
  }
};

formattedInput.forEach((game) => {
  let player1 = game.charAt(0); // a b or c
  let result = game.charAt(2); // x y or z
  let player2 = chooseShape(player1, gameValues[result]); // a b or c

  score += gamePoints[player2] + gamePoints[result];
});

